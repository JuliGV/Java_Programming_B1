package day07_relation_operation;

public class RelationalOperators {

    public static void main(String[] args) {
        System.out.println(5);
        System.out.println(4);

        System.out.println(5>4);
        System.out.println(5<4);
        System.out.println(6>=6);
        System.out.println(7>6);
        System.out.println(6<=6);//true
        System.out.println(6==6);//== - it's equal; = it's assigning operator
        //System.out.println(6=6); this is error
        int a=5; // assigning
        System.out.println(4!=4); //false
        System.out.println(6!=7); //true
    }
}
