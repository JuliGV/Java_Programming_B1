package day05_variables;

public class ConateExample {
    public static void main(String[] args) {

        int numberOne = 4;
        int numberTwo = 5;

        // another type to assign
        int number4 = 5, number3 = 5;

        System.out.println(numberOne + numberTwo );
        // by defolt data type like int
        System.out.println(4+5);
        System.out.println("4" + "5");

        // like characters from table ASCII

        System.out.println('4' + '5');

// int+int+string
        System.out.println(4+5+ "java");
        // int+string+int

        System.out.println(4+"java" +5);
        System.out.println("java"+4+5);

    }
}
