package day3_comments_escape_sequence;

public class TabAndNextLine {
    public static void main(String [] args){

        // This is we type without tab
        System.out.println("This is without tab");

        // This is 1 tab
        System.out.println("\tThis is one tab");

        // This is with two tab
        System.out.println("\t\t This is two tab");

        // Here it is without escape sequence
        System.out.println("    This is without tab");

        // examplas of one line
        System.out.println("");
        System.out.print("Go to ");
        System.out.print("Take honey");
        System.out.print("Delishes");


        System.out.println("Good morning");
        System.out.println("Go take a coffe\ngood morning everyone");


    }
}
